#ifndef STD_TYPES_H_
#define STD_TYPES_H_

// char u s
typedef unsigned char u8;
typedef signed char s8;

// short int u s
typedef unsigned short int u16;
typedef signed short int s16;

// long int u s
typedef unsigned long int u32;
typedef signed long int s32;

// float
typedef float f32;

// double
typedef double f64;

// long double
typedef long double f128;

#endif /* STD_TYPES_H_ */
